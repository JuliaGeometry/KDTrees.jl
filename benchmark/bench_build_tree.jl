using KDtree

dims = 1:6
n_points = [10^i for i in 1:5]

times = Array(Float64, length(dims), length(n_points))

# Compile
KDTree(randn(2,2))

i = 0
for dim in dims
    j = 0
    i+=1
    for n_point in n_points
        j+=1
        data = randn(dim, n_point)
        times[i,j]  = @elapsed KDTree(data)
    end
end

println(times)

